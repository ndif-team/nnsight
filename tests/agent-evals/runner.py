"""Task runner for executing agent-generated code and verifying results.

Handles two task kinds:
- Code tasks: exec setup + agent code, then call task.verify(namespace).
- MCQ tasks: parse the agent's choice from a free-form response, compare to
  the task's correct_index.
"""

import sys
import re
import traceback
import linecache
import itertools
from dataclasses import dataclass
from typing import Optional
from io import StringIO
import signal
from contextlib import contextmanager

from tasks.registry import Task, TaskKind


# Monotonic counter for unique pseudo-filenames per execution. Without this,
# nnsight's per-trace Globals.cache (keyed on filename+lineno) can return a
# stale compiled function from a previous task when several tasks share the
# pseudo-filename in one process (e.g. inside an audit run).
_filename_counter = itertools.count()


@dataclass
class TaskResult:
    """Result of running a single task."""

    task_id: str
    success: bool
    error_message: Optional[str] = None
    execution_output: Optional[str] = None
    verification_passed: bool = False
    code_executed: Optional[str] = None

    def to_dict(self) -> dict:
        return {
            "task_id": self.task_id,
            "success": self.success,
            "error_message": self.error_message,
            "execution_output": self.execution_output,
            "verification_passed": self.verification_passed,
            "code_executed": self.code_executed,
        }


class TimeoutError(Exception):
    """Raised when code execution times out."""

    pass


@contextmanager
def timeout(seconds: int):
    """Context manager for timing out code execution."""

    def signal_handler(signum, frame):
        raise TimeoutError(f"Execution timed out after {seconds} seconds")

    # Set up the signal handler
    old_handler = signal.signal(signal.SIGALRM, signal_handler)
    signal.alarm(seconds)

    try:
        yield
    finally:
        signal.alarm(0)
        signal.signal(signal.SIGALRM, old_handler)


def register_source_with_linecache(filename: str, source: str) -> None:
    """
    Register source code with linecache so inspect.getsourcelines() works.

    This is necessary because nnsight uses inspect.getsourcelines() to capture
    source code for tracing, but exec'd code has no source file. By registering
    the source with linecache, we make it available to the inspect module.

    Args:
        filename: The pseudo-filename to register the source under
        source: The source code to register
    """
    lines = source.splitlines(keepends=True)
    # Ensure each line ends with a newline
    if lines and not lines[-1].endswith("\n"):
        lines[-1] += "\n"
    # Register with linecache - the format is (size, mtime, lines, fullname)
    linecache.cache[filename] = (
        len(source),  # size
        None,  # mtime (None means don't check for updates)
        lines,  # lines
        filename,  # fullname
    )


def execute_code(
    setup_code: str, agent_code: str, timeout_seconds: int = 60
) -> tuple[dict, str]:
    """
    Execute setup code and agent code, returning the namespace and stdout.

    Args:
        setup_code: Code to set up the environment (model loading, imports)
        agent_code: Code generated by the agent
        timeout_seconds: Maximum execution time

    Returns:
        Tuple of (namespace dict, stdout output)
    """
    # Capture stdout
    old_stdout = sys.stdout
    sys.stdout = mystdout = StringIO()

    namespace = {}

    # Create pseudo-filenames for the code.
    # NOTE: These filenames must NOT start with "<nnsight" because nnsight's
    # capture() method skips those when looking for source files. Using names
    # that look like regular Python files allows inspect.getsourcelines() to
    # find the source we registered in linecache.
    # The counter suffix prevents Globals.cache (per-trace AST/code cache,
    # keyed on filename+lineno) from returning a stale compiled function from
    # a previous task when several tasks run in the same process.
    eid = next(_filename_counter)
    setup_filename = f"__nnsight_eval_setup_{eid}__.py"
    agent_filename = f"__nnsight_eval_agent_{eid}__.py"

    try:
        with timeout(timeout_seconds):
            # Register setup code with linecache and compile with filename
            register_source_with_linecache(setup_filename, setup_code)
            setup_compiled = compile(setup_code, setup_filename, "exec")
            exec(setup_compiled, namespace)

            # Register agent code with linecache and compile with filename
            register_source_with_linecache(agent_filename, agent_code)
            agent_compiled = compile(agent_code, agent_filename, "exec")
            exec(agent_compiled, namespace)
    finally:
        sys.stdout = old_stdout
        # Clean up linecache entries
        linecache.cache.pop(setup_filename, None)
        linecache.cache.pop(agent_filename, None)

    output = mystdout.getvalue()
    return namespace, output


def run_task(task, agent_response: str) -> TaskResult:
    """
    Run a single task with the agent's response.

    Dispatches by ``task.kind``:
    - CODE: exec setup + agent code, run verify(namespace).
    - MCQ: parse the agent's choice and compare to correct_index.

    Args:
        task: Task object.
        agent_response: For CODE tasks, the agent's Python code (may still be
            wrapped in markdown — :func:`extract_code_from_response` is run
            for consumers that haven't already stripped fences). For MCQ
            tasks, the agent's free-form pick (a letter, number, or text
            containing one).

    Returns:
        TaskResult with success/failure and details.
    """
    if getattr(task, "kind", TaskKind.CODE) == TaskKind.MCQ:
        return _run_mcq_task(task, agent_response)

    # Code-task path: ``agent_response`` is the agent's Python code.
    agent_code = agent_response
    try:
        # Execute the code
        namespace, output = execute_code(
            task.setup_code, agent_code, timeout_seconds=task.timeout_seconds
        )

        # Run verification
        try:
            verification_passed = task.verify(namespace)
        except Exception as e:
            return TaskResult(
                task_id=task.id,
                success=False,
                error_message=f"Verification error: {str(e)}",
                execution_output=output,
                verification_passed=False,
                code_executed=agent_code,
            )

        return TaskResult(
            task_id=task.id,
            success=verification_passed,
            error_message=None if verification_passed else "Verification failed",
            execution_output=output,
            verification_passed=verification_passed,
            code_executed=agent_code,
        )

    except TimeoutError as e:
        return TaskResult(
            task_id=task.id,
            success=False,
            error_message=str(e),
            execution_output=None,
            verification_passed=False,
            code_executed=agent_code,
        )
    except SyntaxError as e:
        return TaskResult(
            task_id=task.id,
            success=False,
            error_message=f"Syntax error: {str(e)}",
            execution_output=None,
            verification_passed=False,
            code_executed=agent_code,
        )
    except Exception as e:
        return TaskResult(
            task_id=task.id,
            success=False,
            error_message=f"Execution error: {str(e)}\n{traceback.format_exc()}",
            execution_output=None,
            verification_passed=False,
            code_executed=agent_code,
        )


def _run_mcq_task(task: Task, agent_response: str) -> TaskResult:
    """Evaluate an MCQ task by parsing the agent's choice."""
    chosen = parse_mcq_answer(agent_response, n_choices=len(task.choices))
    if chosen is None:
        return TaskResult(
            task_id=task.id,
            success=False,
            error_message=(
                f"Could not parse a choice from the agent response. "
                f"Expected a letter A-{chr(ord('A') + len(task.choices) - 1)} "
                f"or a number 1-{len(task.choices)}."
            ),
            execution_output=None,
            verification_passed=False,
            code_executed=agent_response,
        )

    correct = chosen == task.correct_index
    detail = (
        f"chose {chr(ord('A') + chosen)} (index {chosen}); "
        f"correct = {chr(ord('A') + task.correct_index)} (index {task.correct_index})"
    )
    return TaskResult(
        task_id=task.id,
        success=correct,
        error_message=None if correct else f"Wrong answer: {detail}",
        execution_output=detail,
        verification_passed=correct,
        code_executed=agent_response,
    )


_LETTER_PATTERN = re.compile(r"\b([A-Za-z])\b")
_PAREN_LETTER = re.compile(r"\(([A-Za-z])\)")
_NUMBER_PATTERN = re.compile(r"\b([0-9]+)\b")


def parse_mcq_answer(response: str, n_choices: int) -> Optional[int]:
    """Best-effort parse of an agent's MCQ choice into a 0-based index.

    Accepts:
    - Bare letter on its own line: ``A`` (preferred form).
    - "Answer: A" / "answer: B" / "The answer is C".
    - "(A)" or "A)" inline.
    - A number 1..N (1-based).

    Returns the 0-based index of the chosen option, or ``None`` if no choice
    could be parsed.
    """
    if not response:
        return None
    text = response.strip()

    # Look for explicit "Answer:" / "answer is" / "I choose" forms first.
    m = re.search(
        r"(?:answer|choice|pick|choose|select)\s*(?:is|=|:)?\s*\(?([A-Za-z])\)?",
        text,
        flags=re.IGNORECASE,
    )
    if m:
        letter = m.group(1).upper()
        idx = ord(letter) - ord("A")
        if 0 <= idx < n_choices:
            return idx

    # Parenthesised letter form: "(A)" or "A)".
    m = _PAREN_LETTER.search(text)
    if m:
        idx = ord(m.group(1).upper()) - ord("A")
        if 0 <= idx < n_choices:
            return idx
    m = re.search(r"\b([A-Za-z])\)", text)
    if m:
        idx = ord(m.group(1).upper()) - ord("A")
        if 0 <= idx < n_choices:
            return idx

    # Bare letter — only single-letter responses (whole reply == "A").
    if len(text) <= 3 and text.isalpha():
        idx = ord(text.upper()[0]) - ord("A")
        if 0 <= idx < n_choices:
            return idx

    # First standalone letter token A..<last>.
    m = _LETTER_PATTERN.search(text)
    if m:
        idx = ord(m.group(1).upper()) - ord("A")
        if 0 <= idx < n_choices:
            return idx

    # Fallback: a number 1..N.
    m = _NUMBER_PATTERN.search(text)
    if m:
        n = int(m.group(1))
        if 1 <= n <= n_choices:
            return n - 1

    return None


def extract_code_from_response(response: str) -> str:
    """
    Extract Python code from an LLM response.

    Handles:
    - Code blocks with ```python ... ```
    - Code blocks with ``` ... ```
    - Plain code without markers
    """
    # Try to find python code block
    if "```python" in response:
        start = response.find("```python") + len("```python")
        end = response.find("```", start)
        if end != -1:
            return response[start:end].strip()

    # Try to find generic code block
    if "```" in response:
        start = response.find("```") + 3
        # Skip language identifier if present
        newline = response.find("\n", start)
        if newline != -1:
            start = newline + 1
        end = response.find("```", start)
        if end != -1:
            return response[start:end].strip()

    # Return as-is (assume it's plain code)
    return response.strip()
